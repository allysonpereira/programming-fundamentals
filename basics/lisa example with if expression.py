bag_price=int(input("What is the price of the bag that you want? "))
bank=int(input("How much do you have in the bank? "))
income=int(input("How much of future income? "))
rent=int(input("How much will you have to pay for rent? "))
expenses=int(input("How much do you have to pay in expenses? "))

available=bank+income-rent-expenses
if available>bag_price:
    print("You can buy the bag!")
elif available==bag_price:
    print("You can, but then you won't have any money left!")   
else:
    print("You don't have enough money for the bag!")
    
